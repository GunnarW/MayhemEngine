#include "SkyboxGenerator.h"



SkyboxGenerator::SkyboxGenerator()
{
}


SkyboxGenerator::~SkyboxGenerator()
{
}

void SkyboxGenerator::ConstructSkybox(SkyboxObjectHandle* handle, Renderer* renderer)
{
	std::vector<Texture> textures;
	std::vector<Vertex> verticies = ConstructMesh();

	Texture texture;
	texture.id = renderer->LoadCubeMap(handle, handle->GetTexturePaths());

	textures.push_back(texture);

	handle->GetMeshes()->push_back(Mesh(verticies, textures));
}

std::vector<Vertex> SkyboxGenerator::ConstructMesh()
{
	std::vector<float> positions = 
	 {
		-1.0f,  1.0f, -1.0f,
		-1.0f, -1.0f, -1.0f,
		1.0f, -1.0f, -1.0f,
		1.0f, -1.0f, -1.0f,
		1.0f,  1.0f, -1.0f,
		-1.0f,  1.0f, -1.0f,

		-1.0f, -1.0f,  1.0f,
		-1.0f, -1.0f, -1.0f,
		-1.0f,  1.0f, -1.0f,
		-1.0f,  1.0f, -1.0f,
		-1.0f,  1.0f,  1.0f,
		-1.0f, -1.0f,  1.0f,

		1.0f, -1.0f, -1.0f,
		1.0f, -1.0f,  1.0f,
		1.0f,  1.0f,  1.0f,
		1.0f,  1.0f,  1.0f,
		1.0f,  1.0f, -1.0f,
		1.0f, -1.0f, -1.0f,

		-1.0f, -1.0f,  1.0f,
		-1.0f,  1.0f,  1.0f,
		1.0f,  1.0f,  1.0f,
		1.0f,  1.0f,  1.0f,
		1.0f, -1.0f,  1.0f,
		-1.0f, -1.0f,  1.0f,

		-1.0f,  1.0f, -1.0f,
		1.0f,  1.0f, -1.0f,
		1.0f,  1.0f,  1.0f,
		1.0f,  1.0f,  1.0f,
		-1.0f,  1.0f,  1.0f,
		-1.0f,  1.0f, -1.0f,

		-1.0f, -1.0f, -1.0f,
		-1.0f, -1.0f,  1.0f,
		1.0f, -1.0f, -1.0f,
		1.0f, -1.0f, -1.0f,
		-1.0f, -1.0f,  1.0f,
		1.0f, -1.0f,  1.0f
	};

	std::vector<Vertex> verticies;
	for (unsigned int i = 0; i < positions.size(); i += 3)
	{
		Vertex vertex;
		vertex.Position.x = positions[i];
		vertex.Position.y = positions[i+1];
		vertex.Position.z = positions[i+2];

		verticies.push_back(vertex);
	}

	return verticies;
}
